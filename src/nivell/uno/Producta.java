package nivell.uno;

public class Producta {
	private String name;
	private int preu;
	
	public Producta(String name, int preu) {
		this.name = name;
		this.preu = preu;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPreu() {
		return preu;
	}

	public void setPreu(int preu) {
		this.preu = preu;
	}
	
	
	public String toString() {
		return "";
	}
}
